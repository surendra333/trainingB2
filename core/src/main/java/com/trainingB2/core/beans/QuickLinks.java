package com.trainingB2.core.beans;

public class QuickLinks {

	private String title;
	private String image;
	
	public QuickLinks(String title,String image){
		this.title=title;
		this.image=image;
	}

	public String getTitle() {
		return title;
	}


	public String getImage() {
		return image;
	}

	
}
